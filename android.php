        <h1 class="topsection-title desktop-title mt-5">How do I install Office 365 on my Android device?</h1>
        <div class="page-content-section">
            <p class="para-sections head">Your installation may vary depending on the Android device you have and the Office 365 app you choose to install (however they all require your IIIT email address to license). If you are unable to install Office 365 apps on your Android device using the instructions below, you can try these <strong><a href="https://support.office.com/en-us/article/install-and-set-up-office-on-an-android-cafe9d6f-8b0c-4b03-b20a-12438a82a22d" class="click-here" rel="nofollow">alternate instructions</a></strong>. If you are using a Chromebook, please follow <a href="https://support.microsoft.com/en-us/office/how-to-install-and-run-microsoft-office-on-a-chromebook-32f14a23-2c1a-4579-b973-d4b1d78561ad" class="click-here" rel="nofollow"><strong>these instructions</strong></a>.</p>
