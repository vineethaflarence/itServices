                    <li class="focus-li list-section mt-2 mb-2">From the search results, either select the specific Microsoft Office app you want (<strong>Microsoft Word</strong>, for example). These instructions illustrate how to install the <strong>Microsoft Office</strong> bundle that includes Word, Excel, and PowerPoint.</li>
                    <li class="focus-li list-section mt-2 mb-2">Press <strong>Install.</strong><br><img src="https://i.postimg.cc/WpfqqP8D/an-365-20-2.png" class="li-inner-images"></li>
                    <li class="focus-li list-section mt-2 mb-2">When the install completes, press&nbsp;<strong>Open</strong>.<br><img src="https://i.postimg.cc/bJwdd62H/an-365-20-3.png" class="li-inner-images"></li>
                    <li class="focus-li list-section mt-2 mb-2">Press <strong>ALLOW</strong>&nbsp;(if you press&nbsp;<strong>DENY</strong>, Microsoft prevents you from using the app).<br><img src="https://i.postimg.cc/5tn2cwkH/an-365-20-4.png" class="li-inner-images"></li>
                    <li class="focus-li list-section mt-2 mb-2">Press&<strong>Get Started</strong>.<br><img src="https://i.postimg.cc/52P2TBrX/an-365-20-5.png" class="li-inner-images"></li>
                     <li class="focus-li list-section mt-2 mb-2">Press Next.<br><img src="https://i.postimg.cc/52P2TBrX/an-365-20-5.png" class="li-inner-images"></li>
                    <li class="focus-li list-section mt-2 mb-2">Press <b>DON'T SEND OPTIONAL DATA.</b><br><img src="https://i.postimg.cc/2yFzwhv8/an-365-20-6.png" class="li-inner-images"></li>
                    <li class="focus-li list-section mt-2 mb-2">Press <b>CLOSE.</b><br><img src="https://i.postimg.cc/NjPQpgyT/an-365-20-8.png" class="li-inner-images"></li>
                    <li class="focus-li list-section mt-2 mb-2">Press <strong>CONNECT YOUR ACCOUNT</strong><br><img src="https://i.postimg.cc/tgZpcLPc/an-365-20-9.png" class="li-inner-images"></li>
                    <li class="focus-li list-section mt-2 mb-2">Type in your UMBC email address and then press Next.<br><img src="https://i.postimg.cc/bv7pf39w/an-365-20-10.png" class="li-inner-images"></li>
                    <li class="focus-li list-section mt-2 mb-2">Sign in to IIITH.</li>
